def encrypt(text: str, key: str) -> str:
    ans = ""
    pos = 0
    for x in text:
        i = alphabet.find(x)
        j = alphabet.find(key[pos])
        ans += CipherTable[i][j]
        pos += 1
    return ans


def decrypt(text: str, key: str) -> str:
    ans = ""
    pos = 0
    for x in text:
        j = alphabet.find(key[pos])
        cnt = 0
        for k in CipherTable[:][j]:
            if k == x:
                ans += alphabet[cnt]
                break
            cnt += 1
        pos += 1
    return ans


